// generated_html?
function generatedHtml(path: string, s: BlobSummary): boolean {
  if (!['.html', '.htm', '.xhtml'].includes(extname(path).toLowerCase())) {
    return false
  }
  if (s.lines.length <= 1) return false

  const head = s.lines.slice(0, 31)

  // pkgdown marker (first 2 lines)
  if (
    head
      .slice(0, 2)
      .some((l) => /<!-- Generated by pkgdown: do not edit by hand -->/.test(l))
  ) {
    return true
  }
  // Mandoc marker (line 2)
  if (
    s.lines.length > 2 &&
    s.lines[2]?.startsWith('<!-- This is an automatically generated file.')
  ) {
    return true
  }
  // Doxygen marker (first 31 lines)
  if (head.some((l) => /<!--\s+Generated by Doxygen\s+[.0-9]+\s*-->/i.test(l))) {
    return true
  }

  // <meta name="generator" content="..."> style
  const joined = head.join(' ')
  const metaMatches = [...joined.matchAll(/<meta(\s+[^>]+)>/gi)]
  if (metaMatches.length === 0) return false
  return metaMatches.some((m) => {
    const attrs = extractHtmlMeta(m[1] ?? '')
    if ((attrs['name'] ?? '').toLowerCase() !== 'generator') return false
    const cv = [attrs['content'], attrs['value']].filter((v): v is string => !!v)
    return cv.some((v) =>
      /^\s*(?:org\s+mode|j?latex2html|groff|makeinfo|texi2html|ronn)\b/i.test(v),
    )
  })
}

function extractHtmlMeta(attrStr: string): Record<string, string> {
  const result: Record<string, string> = {}
  const cleaned = attrStr.replace(/\/\s*$/, '').trim()
  const re = /(?<=^|\s)(name|content|value)\s*=\s*("[^"]+"|'[^']+'|[^\s"']+)/gi
  for (const m of cleaned.matchAll(re)) {
    const key = m[1]!.toLowerCase()
    let val = m[2]!
    val = val.replace(/^["']|["']$/g, '')
    result[key] = val
  }
  return result
}

