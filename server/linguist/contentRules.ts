/**
 * Linguist's content-based "generated" rules — the ones that need to
 * inspect file contents (first/last lines, line counts, etc.) rather
 * than filenames.
 *
 * Ported from:
 *   github-linguist/linguist @ 537297cdae3ab05f8d5dd1c03627a5bd73707b19
 *   - lib/linguist/generated.rb
 *
 * Every rule only sets `generated` — Linguist's vendored and
 * documentation checks are path-based and live in `pathRules.ts`.
 */

import type { FileAttrs } from '../../shared/types.ts'
import type { BlobSummary } from './contentReader.ts'

export function classifyByContent(path: string, summary: BlobSummary): Partial<FileAttrs> {
  return isGenerated(path, summary) ? { generated: true } : {}
}

function isGenerated(path: string, s: BlobSummary): boolean {
  return (
    minified(path, s) ||
    hasSourceMapRef(path, s) ||
    isSourceMap(path, s) ||
    compiledCoffeescript(path, s) ||
    generatedNetDocfile(path, s) ||
    generatedParser(path, s) ||
    generatedPostscript(path, s) ||
    generatedGo(path, s) ||
    generatedProtocolBufferFromGo(path, s) ||
    generatedProtocolBuffer(path, s) ||
    generatedJavascriptProtocolBuffer(path, s) ||
    generatedTypescriptProtocolBuffer(path, s) ||
    generatedApacheThrift(path, s) ||
    generatedJniHeader(path, s) ||
    vcrCassette(path, s) ||
    generatedAntlr(path, s) ||
    compiledCythonFile(path, s) ||
    generatedModule(path, s) ||
    generatedUnity3dMeta(path, s) ||
    generatedRacc(path, s) ||
    generatedJflex(path, s) ||
    generatedGrammarkit(path, s) ||
    generatedRoxygen2(path, s) ||
    generatedJison(path, s) ||
    generatedGrpcCpp(path, s) ||
    generatedDart(path, s) ||
    generatedPerlPpportHeader(path, s) ||
    generatedGamemakerstudio(path, s) ||
    generatedGimp(path, s) ||
    generatedVisualstudio6(path, s) ||
    generatedHaxe(path, s) ||
    generatedHtml(path, s) ||
    generatedJooq(path, s) ||
    generatedSorbetRbi(path, s) ||
    generatedMysqlViewDefinitionFormat(path, s)
  )
}

// --- Path helpers ---

function extname(path: string): string {
  const slash = path.lastIndexOf('/')
  const base = slash === -1 ? path : path.slice(slash + 1)
  const dot = base.lastIndexOf('.')
  return dot === -1 ? '' : base.slice(dot)
}

function basename(path: string): string {
  const slash = path.lastIndexOf('/')
  return slash === -1 ? path : path.slice(slash + 1)
}

// --- Rule implementations (preserving Ruby source order where practical) ---

const MAYBE_MINIFIED = new Set(['.js', '.css'])

function maybeMinified(path: string): boolean {
  return MAYBE_MINIFIED.has(extname(path).toLowerCase())
}

// minified_files?
function minified(path: string, s: BlobSummary): boolean {
  if (!maybeMinified(path)) return false
  if (s.lines.length === 0) return false
  const total = s.lines.reduce((acc, l) => acc + l.length, 0)
  return total / s.lines.length > 110
}

// has_source_map?
function hasSourceMapRef(path: string, s: BlobSummary): boolean {
  if (!maybeMinified(path)) return false
  const tail = s.lines.slice(-2)
  // eslint-disable-next-line unicorn/prefer-string-starts-ends-with
  return tail.some((l) => /^\/[*/][#@] source(?:Mapping)?URL|sourceURL=/.test(l))
}

// source_map?
function isSourceMap(path: string, s: BlobSummary): boolean {
  if (extname(path).toLowerCase() !== '.map') return false
  if (/(\.css|\.js)\.map$/i.test(path)) return true
  const first = s.lines[0] ?? ''
  // eslint-disable-next-line unicorn/prefer-string-starts-ends-with
  return /^{"version":\d+,/.test(first) || first.startsWith('/** Begin line maps. **/{')
}

// compiled_coffeescript?
function compiledCoffeescript(path: string, s: BlobSummary): boolean {
  if (extname(path) !== '.js') return false
  const first = s.lines[0] ?? ''
  if (first.startsWith('// Generated by ')) return true
  if (s.lines.length < 3) return false
  if (first !== '(function() {') return false
  if (s.lines[s.lines.length - 2] !== '}).call(this);') return false
  if (s.lines[s.lines.length - 1] !== '') return false

  let score = 0
  for (const line of s.lines) {
    if (!/var /.test(line)) continue
    score += countMatches(line, /_fn|_i|_len|_ref|_results/g)
    score += 3 * countMatches(line, /__bind|__extends|__hasProp|__indexOf|__slice/g)
  }
  return score >= 3
}

function countMatches(s: string, re: RegExp): number {
  return (s.match(re) ?? []).length
}

// generated_net_docfile?
function generatedNetDocfile(path: string, s: BlobSummary): boolean {
  if (extname(path).toLowerCase() !== '.xml') return false
  if (s.lines.length <= 3) return false
  return (
    (s.lines[1]?.includes('<doc>') ?? false) &&
    (s.lines[2]?.includes('<assembly>') ?? false) &&
    (s.lines[s.lines.length - 2]?.includes('</doc>') ?? false)
  )
}

// generated_parser?  (PEG.js)
function generatedParser(path: string, s: BlobSummary): boolean {
  if (extname(path) !== '.js') return false
  const head = s.lines.slice(0, 5).join('')
  return /^(?:[^/]|\/[^*])*\/\*(?:[^*]|\*[^/])*Generated by PEG\.js/.test(head)
}

// generated_postscript?
function generatedPostscript(path: string, s: BlobSummary): boolean {
  if (!['.ps', '.eps', '.pfa'].includes(extname(path))) return false

  // Full content check for the two hex-stream markers.
  const joined = s.lines.join('\n')
  if (/^\s*(?:currentfile eexec\s+|\/sfnts\s+\[\s<)/m.test(joined)) return true

  const head = s.lines.slice(0, 10)
  const creator = head.find((l) => l.startsWith('%%Creator: '))
  if (!creator) return false

  if (
    /[0-9]|draw|mpage|ImageMagick|inkscape|MATLAB/.test(creator) ||
    /PCBNEW|pnmtops|\(Unknown\)|Serif Affinity|Filterimage -tops/.test(creator)
  ) {
    return true
  }

  // EAGLE check
  if (creator.includes('EAGLE')) {
    return s.lines.slice(0, 5).some((l) => l.startsWith('%%Title: EAGLE Drawing '))
  }
  return false
}

// generated_go?
function generatedGo(path: string, s: BlobSummary): boolean {
  if (extname(path) !== '.go') return false
  if (s.lines.length <= 1) return false
  return s.lines.slice(0, 40).some((l) => /^\/\/ Code generated .*/.test(l))
}

// generated_protocol_buffer_from_go?
function generatedProtocolBufferFromGo(path: string, s: BlobSummary): boolean {
  if (extname(path) !== '.proto') return false
  if (s.lines.length <= 1) return false
  return s.lines
    .slice(0, 20)
    .some((l) => l.includes('This file was autogenerated by go-to-protobuf'))
}

const PROTOBUF_EXTS = new Set(['.py', '.java', '.h', '.cc', '.cpp', '.m', '.rb', '.php'])

// generated_protocol_buffer?
function generatedProtocolBuffer(path: string, s: BlobSummary): boolean {
  if (!PROTOBUF_EXTS.has(extname(path))) return false
  if (s.lines.length <= 1) return false
  return s.lines
    .slice(0, 3)
    .some((l) => l.includes('Generated by the protocol buffer compiler.  DO NOT EDIT!'))
}

