// generated_sorbet_rbi?  (Tapioca)
function generatedSorbetRbi(path: string, s: BlobSummary): boolean {
  if (extname(path).toLowerCase() !== '.rbi') return false
  if (s.lines.length < 5) return false
  if (!(s.lines[0] ?? '').startsWith('# typed:')) return false
  // Ruby's implementation checks for Tapioca's standard header in the next
  // few lines. See the source; we accept any of the three Tapioca variants.
  return s.lines
    .slice(1, 5)
    .some((l) =>
      /DO NOT EDIT MANUALLY|This is an autogenerated file|DO NOT EDIT: This file is automatically generated/i.test(
        l,
      ),
    )
}

